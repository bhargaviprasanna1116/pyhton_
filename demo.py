#double quotes
name="bhargavi"
print(name)
print(type(name))
print("----------------")
#single quotes
name='madhu'
print(name)
print(type(name))
#triple single quotes
print("-----------------")
name='''hanu'''
print(name)
print(type(name))
print("------------------")
#triple double quotes
name="""bhanu"""
print(name)
print(type(name))
print("-------------------")
#double within single quotes
name="bhanu is 'dancing'"
print(name)
print(type(name))
print("--------------------")
#single within double quotes
name='sandhya is "dancing"'
print(name)
print(type(name))
print("--------------------")
#escape character
name="hi \n Had dinner?"
print(name)
print(type(name))
print("--------------------")
#raw string
name=r"ramya is dancing"
print(name)
print(type(name))
